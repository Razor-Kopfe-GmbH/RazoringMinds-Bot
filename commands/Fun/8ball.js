module.exports.run = async (bot, message, args) => {
    if (!args[0]) return message.channel.send(`Hello, ${message.author.username}, unfortunatley you have to type something for the 8ball, **idiot**.`)
    const i_make = ["Yes", "No", "Maybe", "Ask the mods", "Who cares lmao"]
    message.channel.send(i_make[Math.floor(Math.random() * i_make.length)])
}

module.exports.help = {
    name: "8ball",
    description: "Ask magic 8ball.",
    usage: "8ball <question>",
    type: "Utility"
}