module.exports.run = async (bot, message, args) => {
    const botUser = message.guild.members.cache.get(bot.user.id);
    if (!message.member.hasPermission("MANAGE_MESSAGES")) return message.channel.send('You\'re missing Manage Messages permission.')
    if (!botUser.hasPermission("MANAGE_MESSAGES")) return message.channel.send("Bot is missing permission to clear messages.")
    if (!args[0]) return message.reply('NUMBER OF MESAGES noob')
    message.channel.bulkDelete(args[0]);
    message.delete();
}

module.exports.help = {
    name: "clear",
    description: "Clear a message or messages.",
    usage: "clear {Number of Messages}",
    type: "Utility"
}