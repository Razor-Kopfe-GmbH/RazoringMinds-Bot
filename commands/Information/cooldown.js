const commandRecently = require("../Economy/work.js");
const usedCommandRecently = commandRecently.commandrecent.usedCommandRecently;

module.exports.run = async (bot, message, args) => {
    if (usedCommandRecently.has(message.author.id)) {
        message.reply("❌You cannot use that commands for 30 seconds !❌");
    } else {
        message.channel.send('✅You are not on cooldown this is a custom command✅');

        usedCommandRecently.add(message.author.id);
        setTimeout(() => {
            usedCommandRecently.delete(message.author.id)
        }, 30000);
    }
}

module.exports.help = {
    name: "cooldown",
    description: "List your cooldown.",
    usage: "cooldown",
    type: "Utility"
}